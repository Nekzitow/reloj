    public void SetmodeloImple(){
        jTable1.setModel(Modelo);
        Modelo.addColumn("NOMBRE");
        Modelo.addColumn("PROFESION");
        jTable1.getTableHeader().setReorderingAllowed(false);
    }
    public void LlenartablaImple(){
        try{
            int num=jTable1.getRowCount();
            for( int i = num - 1 ; i >= 0 ; i-- ){
                Modelo.removeRow( i );
            }
            ArrayList<Empleado> emp = Empleado.listaEmpleados(con, 1);
            for (Empleado empleado : emp) {
                Modelo.addRow(new Object[]{empleado,empleado.getProfesion()});
            }
        }catch( Exception e ){
            e.printStackTrace();
        }
    }
