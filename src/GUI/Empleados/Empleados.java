    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        for(int i=jTable1.getRowCount()-1;i>=0;i--){
            Modelo.removeRow(i);
        }
        String txt = jTextField1.getText();
        ArrayList<Empleado> emp = Empleado.filtroEmpleados(con, 1,txt);
        for (Empleado empleado : emp) {
            Modelo.addRow(new Object[]{empleado,empleado.getProfesion()});
        }
    }//GEN-LAST:event_jTextField1KeyReleased

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int fila = jTable1.getSelectedRow();
        if(fila >=0 ){
            Empleado empleado = (Empleado)jTable1.getValueAt(fila, 0);
            GuiCaptarHUella captar = new GuiCaptarHUella(this.con, empleado,this.principal);
            this.principal.jDesktopPane1.add(captar);
            captar.show();
            this.dispose();
        }else
            JOptionPane.showMessageDialog(this, "Debe seleccionar un registro de la lista"
            ,"Aviso",JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_jButton1ActionPerformed


